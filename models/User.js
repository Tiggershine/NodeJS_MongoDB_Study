const mongoose = require('mongoose')


const userSchema = monggose.Schema({
  name: {
    type: String,
    maxlength: 50
  },
  email: {
    type: String,
    trim: true,
    unique: 1
  },
  lastname: {
    type: String,
    maxlength: 50
  },
  role: {
    type: Number,
    default: 0
  },
  image: String,
  token: {
    type: String
  },
  tokenExp: {
    type: Number
  }
})

/* Model은 Schema를 감싸주는 역할 */ 
const User = mongoose.model('User', userSchema)
